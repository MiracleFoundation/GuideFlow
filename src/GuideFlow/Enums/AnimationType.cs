namespace GuideFlow.Enums;

/// <summary>
/// Animation type for step popover appearance.
/// </summary>
public enum AnimationType
{
    /// <summary>
    /// Fade in with slight upward movement (default).
    /// </summary>
    Fade,

    /// <summary>
    /// Slide in from the direction of the placement.
    /// </summary>
    Slide,

    /// <summary>
    /// Bounce in effect.
    /// </summary>
    Bounce,

    /// <summary>
    /// No animation.
    /// </summary>
    None
}
